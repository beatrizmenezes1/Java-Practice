// TRABALHANDO COM LOOPS

// While - Enquanto faça alguma coisa

x = 0;

while(x <= 10){
    document.write("<br> O valor do X é: "+ x);
    x++;
}

document.write('<br><br><br> O X esta valendo: '+x+"<br><br><br>");

// For - Para

for(a = 0; a <= 10; a++){
    document.write("<br> O valor do A é: "+ a);
}

document.write('<br><br><br> O A esta valendo: '+a+"<br><br><br>");